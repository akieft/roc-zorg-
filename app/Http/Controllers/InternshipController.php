     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $id = $request->id;
        $internship = Internship::find($id);
        $internship->coach_permission = 1;
        $internship->save();
        return redirect('internship');
    }

